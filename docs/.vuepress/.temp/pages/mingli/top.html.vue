<h3 id="格局论顺逆" tabindex="-1"><a class="header-anchor" href="#格局论顺逆" aria-hidden="true">#</a> 格局论顺逆</h3>
<div class="custom-container tip"><p class="custom-container-title">善者善用，逆者逆用。因阴阳相生相克所以为善，同性而成恶。用之为宜，皆有成就。</p>
<p>举个例子，克日主之官杀，阴阳克之有情是谓节制，规劝之意。同而无情是为虐待，强迫。有情无情定之顺逆，所以正官喜得财生，或生印，生谓之有情。得财生者，即官得财生格局。生印者谓官印相生。</p>
<p>其中也有特殊的，比如食神是同性而善，财和印不是特别需要区分偏正。官杀是最需要区分的，再有就是劫财羊刃。食伤虽然可以同论，不过伤官喜印，食神喜日主和财或七杀也是需要区分的，一旦食伤混杂，优先取印。</p>
</div>
