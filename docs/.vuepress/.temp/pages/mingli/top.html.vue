<p>根据我的分析，不一定可靠，强弱看的是日主是否有 <strong>自主权</strong>，而旺衰更多看的是格局的 <strong>配合</strong>，日主能否去生泄，或受财官主要看就是坐支。<br>
举个例子，丙生在巳午月和坐巳午支的情况，在月令，月令看的是格局之需，禄了可以财官印都行，如果是刃，七杀最佳。如果是坐支，可以配合月令，受官杀财食伤。</p>
