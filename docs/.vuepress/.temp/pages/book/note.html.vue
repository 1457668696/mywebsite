<h3 id="咸" tabindex="-1"><a class="header-anchor" href="#咸" aria-hidden="true">#</a> 咸</h3>
<div class="custom-container tip"><p class="custom-container-title">盐是百味之首，除了甜味以外，其他味都以咸为基础。解腻、提鲜、去腥。</p>
