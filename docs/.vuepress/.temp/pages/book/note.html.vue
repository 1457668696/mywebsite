<li>盐腌制腊肉，能保存很久。</li>
<li>酱油生抽提鲜、老抽上色。</li>
</ul>
<h3 id="甜" tabindex="-1"><a class="header-anchor" href="#甜" aria-hidden="true">#</a> 甜</h3>
<div class="custom-container tip"><p class="custom-container-title">调和诸味。解腻、缓辣、抑苦。甘而不浓，甜而不腻、放糖不显甜。</p>
<p>白糖、冰糖、红糖、蜂糖、饴糖、果酱、蜜饯</p>
</div>
<h4 id="用法-1" tabindex="-1"><a class="header-anchor" href="#用法-1" aria-hidden="true">#</a> 用法</h4>
